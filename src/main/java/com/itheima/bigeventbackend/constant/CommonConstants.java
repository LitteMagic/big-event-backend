package com.itheima.bigeventbackend.constant;

public final class CommonConstants {
//    时间常量
    public static final long TIME_PER_SECOND = 1000L; //秒 1000毫秒
    public static final long TIME_PER_MINUTE = 60L * TIME_PER_SECOND; //分钟 60秒
    public static final long TIME_PER_HOUR = 60L * TIME_PER_MINUTE;  //小时 60分钟
    public static final long TIME_PER_DAY = 24L * TIME_PER_HOUR; //天 24小时
    public static final long  TIME_PER_WEEK = 7L * TIME_PER_DAY; //周 7天
    public static final long  TIME_PER_MONTH= 30L * TIME_PER_DAY; //月 30天 ()

}
